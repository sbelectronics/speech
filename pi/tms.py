def showhelp():
    print("Usage: tms.py [a|d|g|l|r|s|t|z]")
    print("  a: say external 'acorn'")
    print("  d: dump memory for acorn")
    print("  g: say external 'good morning'")
    print("  l: say external letters")
    print("  r: say rom 'this is a program running on the computer'")
    print("  s: say external 'scott was here'")
    print("  t: time test")
    print("  z: say external 'zero'")

def main(args):
    tms = tms5220.TMS5220(0)
    time.sleep(0.3)
    continuous = False
    while True:
        for arg in args:
            larg = arg.lower()
            if (larg=="a"): 
                tms.sayExternal(ACORN)
            elif (larg=="d"):
                tms.dump(0xBB6)
            elif (larg=="g"):
                tms.sayExternal(GOOD)
                tms.sayExternal(MORNING)
            elif (larg=="l"):
                tms.sayWords(MLETTR)
            elif (larg=="r"):
                tms.sayWords(MTEST)
            elif (larg=="s"):
                tms.sayExternal(SCOTT)
                tms.sayExternal(WAS)
                tms.sayExternal(HERE)
            elif (larg=="t"):
                tms.sayExternal(THE)
                tms.sayExternal(TIME)
                tms.sayExternal(IS)
                tms.sayExternal(ONE)
                tms.sayExternal(TWENTY)
                tms.sayExternal(THREE)
                tms.sayExternal(PM)
            elif (larg=="z"):
                tms.sayExternal(ZERO)
            elif (larg=="c"):
                continuous = True
            else:
                print("Unknown command: %s" % arg)
                showhelp()
                sys.exit(-1)
        if not continuous:
            break

    if len(args) == 0:
        showhelp()

if __name__ == "__main__":
    main(sys.argv[1:])
